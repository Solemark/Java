public class Calculator {
    /**
     * add x and y
     * 
     * @param x
     * @param y
     * @return float
     */
    public float addition(float x, float y) {
        return x + y;
    }

    /**
     * subtract y from x
     * 
     * @param x
     * @param y
     * @return float
     */
    public float subtraction(float x, float y) {
        return x - y;
    }

    /**
     * multiply x by y
     * 
     * @param x
     * @param y
     * @return float
     */
    public float multiplication(float x, float y) {
        return x * y;
    }

    /**
     * 
     * @param x
     * @param y
     * @return float
     */
    public float division(float x, float y) {
        return x / y;
    }
}
